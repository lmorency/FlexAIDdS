inline constexpr int NUM_SYBYL_ENTRIES = sizeof(kSybylTypes) / sizeof(kSybylTypes[0]);

/// Look up SYBYL type number from type string (case-insensitive).
/// Returns type_number and writes radius. Defaults to DUMMY (39, 2.0) on miss.
inline int sybyl_type_lookup(std::string_view name, float& radius_out) {
    // Uppercase the name for matching
    // (names in the table are already uppercase)
    char upper[16]{};
    int len = static_cast<int>(name.size());
    if (len > 15) len = 15;
    for (int i = 0; i < len; ++i) {
        char c = name[i];
        upper[i] = (c >= 'a' && c <= 'z') ? static_cast<char>(c - 32) : c;
    }
    // Remove spaces
    char clean[16]{};
    int ci = 0;
    for (int i = 0; i < len; ++i)
        if (upper[i] != ' ') clean[ci++] = upper[i];
    std::string_view sv(clean, ci);

    for (int i = 0; i < NUM_SYBYL_ENTRIES; ++i) {
        if (kSybylTypes[i].name == sv) {
            radius_out = kSybylTypes[i].radius;
            return kSybylTypes[i].type_number;
        }
    }
    radius_out = 2.0f;
    return 39; // DUMMY
}

