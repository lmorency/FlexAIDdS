#!/usr/bin/env bash
# setup_cloud_storage.sh — One-time setup for FlexAIDdS benchmark storage
#
# Creates directory hierarchy on iCloud Drive (primary) and Google Drive (mirror),
# sets up environment variables, and validates paths.
#
# Usage:
#   ./benchmarks/m3pro/setup_cloud_storage.sh
#
# Apache-2.0 (c) 2026 NRGlab, Universite de Montreal

set -euo pipefail

# ─── Colors ──────────────────────────────────────────────────────────────────

RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
CYAN='\033[0;36m'
NC='\033[0m'

info()  { printf "${CYAN}[INFO]${NC}  %s\n" "$*"; }
ok()    { printf "${GREEN}[OK]${NC}    %s\n" "$*"; }
warn()  { printf "${YELLOW}[WARN]${NC}  %s\n" "$*"; }
die()   { printf "${RED}[ERROR]${NC} %s\n" "$*" >&2; exit 1; }

# ─── Detect iCloud Drive ────────────────────────────────────────────────────

ICLOUD_ROOT="$HOME/Library/Mobile Documents/com~apple~CloudDocs"
if [[ ! -d "$ICLOUD_ROOT" ]]; then
    die "iCloud Drive not found at: $ICLOUD_ROOT"
fi
ICLOUD_BASE="$ICLOUD_ROOT/FlexAIDdS"
info "iCloud Drive root: $ICLOUD_ROOT"

# ─── Detect Google Drive ────────────────────────────────────────────────────

GDRIVE_ROOT=""
for d in "$HOME/Library/CloudStorage"/GoogleDrive-*/; do
    if [[ -d "$d" ]]; then
        # Prefer "My Drive" subfolder if it exists
        if [[ -d "${d}My Drive" ]]; then
            GDRIVE_ROOT="${d}My Drive"
        else
            GDRIVE_ROOT="$d"
        fi
        break
    fi
done

if [[ -z "$GDRIVE_ROOT" ]]; then
    die "Google Drive not found at ~/Library/CloudStorage/GoogleDrive-*/"
fi
GDRIVE_BASE="$GDRIVE_ROOT/FlexAIDdS"
info "Google Drive root: $GDRIVE_ROOT"

# ─── Create directory hierarchy ──────────────────────────────────────────────

DIRS=(
    "build"
    "benchmark_data"
    "results/kernels"
    "results/tier1"
    "results/tier2"
    "logs"
)

info "Creating iCloud directory tree..."
for d in "${DIRS[@]}"; do
    mkdir -p "$ICLOUD_BASE/$d"
done
ok "iCloud tree created at: $ICLOUD_BASE"

# Google Drive mirrors everything except build/
info "Creating Google Drive directory tree..."
for d in "${DIRS[@]}"; do
    [[ "$d" == "build" ]] && continue
    mkdir -p "$GDRIVE_BASE/$d"
done
ok "Google Drive tree created at: $GDRIVE_BASE"

# ─── Detect repo root ───────────────────────────────────────────────────────

SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"
REPO_ROOT="$(cd "$SCRIPT_DIR/../.." && pwd)"

if [[ ! -f "$REPO_ROOT/CMakeLists.txt" ]]; then
    die "Cannot find repo root (expected CMakeLists.txt at $REPO_ROOT)"
fi
info "Repo root: $REPO_ROOT"

# ─── Create convenience symlinks in repo ─────────────────────────────────────

create_symlink() {
    local target="$1"
    local link="$2"
    if [[ -L "$link" ]]; then
        rm "$link"
    elif [[ -e "$link" ]]; then
        warn "Skipping symlink: $link already exists and is not a symlink"
        return
    fi
    ln -s "$target" "$link"
    ok "Symlink: $link -> $target"
}

create_symlink "$ICLOUD_BASE/build" "$REPO_ROOT/build"
create_symlink "$ICLOUD_BASE/results" "$REPO_ROOT/results"

# ─── Write environment file ─────────────────────────────────────────────────

ENV_FILE="$HOME/.flexaidds_env"

cat > "$ENV_FILE" << ENVEOF
# FlexAIDdS M3 Pro Benchmark Environment
# Generated by setup_cloud_storage.sh on $(date -u +%Y-%m-%dT%H:%M:%SZ)

export FLEXAIDDS_REPO="$REPO_ROOT"
export FLEXAIDDS_ICLOUD="$ICLOUD_BASE"
export FLEXAIDDS_GDRIVE="$GDRIVE_BASE"
export FLEXAIDDS_BENCHMARK_DATA="$ICLOUD_BASE/benchmark_data"
export FLEXAIDDS_BINARY="$ICLOUD_BASE/build/FlexAID"
export FLEXAIDDS_RESULTS="$ICLOUD_BASE/results"
export FLEXAIDDS_LOGS="$ICLOUD_BASE/logs"
export FLEXAIDDS_BUILD="$ICLOUD_BASE/build"
ENVEOF

ok "Environment file written: $ENV_FILE"

# ─── Add source to .zshrc if not present ─────────────────────────────────────

ZSHRC="$HOME/.zshrc"
SOURCE_LINE='[[ -f "$HOME/.flexaidds_env" ]] && source "$HOME/.flexaidds_env"'

if [[ -f "$ZSHRC" ]] && grep -qF '.flexaidds_env' "$ZSHRC"; then
    info ".zshrc already sources .flexaidds_env"
else
    echo "" >> "$ZSHRC"
    echo "# FlexAIDdS benchmark environment" >> "$ZSHRC"
    echo "$SOURCE_LINE" >> "$ZSHRC"
    ok "Added source line to $ZSHRC"
fi

# ─── Validate writability ───────────────────────────────────────────────────

ICLOUD_TEST="$ICLOUD_BASE/logs/.write_test_$$"
GDRIVE_TEST="$GDRIVE_BASE/logs/.write_test_$$"

if touch "$ICLOUD_TEST" 2>/dev/null; then
    rm -f "$ICLOUD_TEST"
    ok "iCloud is writable"
else
    die "iCloud path is NOT writable: $ICLOUD_BASE"
fi

if touch "$GDRIVE_TEST" 2>/dev/null; then
    rm -f "$GDRIVE_TEST"
    ok "Google Drive is writable"
else
    die "Google Drive path is NOT writable: $GDRIVE_BASE"
fi

# ─── Summary ─────────────────────────────────────────────────────────────────

echo ""
echo "================================================================"
echo "  FlexAIDdS Cloud Storage Setup Complete"
echo "================================================================"
echo ""
echo "  iCloud (PRIMARY):  $ICLOUD_BASE"
echo "  Google Drive (MIRROR): $GDRIVE_BASE"
echo ""
echo "  Environment:       $ENV_FILE"
echo "  Repo symlinks:     build -> iCloud, results -> iCloud"
echo ""
echo "  Directories:"
for d in "${DIRS[@]}"; do
    echo "    $d/"
done
echo ""
echo "  Next steps:"
echo "    source ~/.flexaidds_env"
echo "    ./benchmarks/m3pro/build_m3pro.sh"
echo "    ./benchmarks/m3pro/run_benchmarks.sh"
echo ""
echo "================================================================"
